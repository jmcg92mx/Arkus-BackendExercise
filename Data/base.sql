CREATE TABLE IF NOT EXISTS "Transaction_Status" (
	"Key"	INTEGER NOT NULL,
	"Value"	TEXT NOT NULL,
	PRIMARY KEY("Key" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Payments" (
	"PaymentId"	INTEGER NOT NULL,
	"Invoice"	INTEGER NOT NULL,
	"PaymentDate"	REAL NOT NULL,
	PRIMARY KEY("PaymentId" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Transactions" (
	"TransactionId"	INTEGER NOT NULL,
	"To"	TEXT NOT NULL,
	"Amount"	REAL NOT NULL,
	"Date"	REAL NOT NULL UNIQUE,
	"Status"	INTEGER NOT NULL DEFAULT 1 COLLATE BINARY,
	"Description"	TEXT NOT NULL COLLATE BINARY,
	PRIMARY KEY("TransactionId" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Invoices" (
	"InvoiceId"	INTEGER NOT NULL,
	"Transaction"	INTEGER NOT NULL,
	"Date"	REAL NOT NULL,
	"IsPaid"	INTEGER NOT NULL DEFAULT 0,
	PRIMARY KEY("InvoiceId" AUTOINCREMENT)
);
